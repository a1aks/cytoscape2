package cytoscape.data;
//-----------------------------------------------------------------------------
import java.io.*;
//-----------------------------------------------------------------------------
public class GeneSynonym implements Serializable {
  String name, value;
//-----------------------------------------------------------------------------
public GeneSynonym (String name, String value)
{
  this.name = name;
  this.value = value;

}
//-----------------------------------------------------------------------------
public String getName ()
{
  return name;
}
//-----------------------------------------------------------------------------
public String getValue ()
{
  return value;
}
//-----------------------------------------------------------------------------
} // GeneSynonym
